async function main() {
  console.log(`\n🌱  TaDa Staging Seed Script`);
  console.log(`   API: ${API_URL}\n`);

  // Create properties
  const results = { ok: [], failed: [] };

  for (let i = 0; i < PROPERTIES.length; i++) {
    const p = PROPERTIES[i];
    process.stdout.write(`   [${String(i + 1).padStart(2, "0")}/${PROPERTIES.length}] ${p.title} ... `);
    try {
      const created = await post("/properties", p);
      console.log(`✅  id=${created.id}`);
      results.ok.push(created.id);
    } catch (err) {
      console.log(`❌  ${err.message}`);
      results.failed.push({ title: p.title, error: err.message });
    }
  }

  // 3. Summary
  console.log(`\n📊  Done: ${results.ok.length} created, ${results.failed.length} failed`);
  if (results.failed.length > 0) {
    console.log("\nFailed:");
    results.failed.forEach((f) => console.log(`  - ${f.title}: ${f.error}`));
  }

  console.log("\n💡  Tip: log in to staging and check /app/units — with Best Match sort, page 1");
  console.log("    should always show the highest-scoring properties regardless of pagination.\n");
}

