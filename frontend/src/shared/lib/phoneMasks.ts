/**
 * Get phone mask for a country code
 * @param countryCode - ISO 3166-1 alpha-2 country code (e.g., "US", "GB", "FR")
 * @returns Phone mask string for InputMask component, or default mask if not found
 */
export function getPhoneMask(countryCode: string): string {
  return (
    phoneMasks[countryCode.toUpperCase()] || phoneMasks["US"] || "(000)000-0000"
  );
}
